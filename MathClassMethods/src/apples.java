
public class apples {
	public static void main(String[] args) {
		System.out.println(Math.abs(-37.5)); //絕對值
		System.out.println(Math.ceil(7.4)); //無條件進位
		System.out.println(Math.floor(7.8)); //無條件捨去
		System.out.println(Math.max(26.9, 56.4));
		System.out.println(Math.min(77.6, 3.45));
		System.out.println(Math.pow(5, 3)); //指數 ex:5^3
		System.out.println(Math.sqrt(9)); //開根號
	}
}
