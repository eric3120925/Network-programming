
public class Student {
	public String id ;
	public String name;
	public String score;
	
	public Student(String i , String n ,String s) {
		id = i;
		name =n;
		score =s;
	}
	
	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getScore() {
		return score;
	}
	
}
